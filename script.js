// Add success message for contact info clicks in footer
document.querySelectorAll('.footer .contact-item').forEach(item => {
    item.addEventListener('click', function() {
        const contactText = this.querySelector('span').textContent;
        
        if (contactText.includes('@')) {
            window.open(`mailto:${contactText}`, '_blank');
        } else if (contactText.includes('+7')) {
            window.open(`tel:${contactText}`, '_blank');
        }
    });
}); 